    private void upBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upBtnActionPerformed
        try {
            String name = txtNameEd.getText();
            int age = Integer.parseInt(txtAgeEd.getText());
            Statement stmt1 = con.createStatement();
            ResultSet rs = stmt1.executeQuery("SELECT id FROM users WHERE name='"+name+"'");
            
            while(rs.next()){
                int id = rs.getInt("id");
            PreparedStatement stmt = con.prepareStatement("Update users set age=? , name=? where id=?");
            stmt.setInt(1,age);
            stmt.setString(2, name);
            
            stmt.setInt(3,id);
            stmt.executeUpdate();
            System.out.println("Updated Successfully");
            }
           
           
            listBtn.doClick();
        } catch (SQLException ex) {
            Logger.getLogger(ProjectJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_upBtnActionPerformed

