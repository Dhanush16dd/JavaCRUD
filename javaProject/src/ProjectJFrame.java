    private void dleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dleBtnActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) table1.getModel();
            int row = table1.getSelectedRow();
            String name = (String) model.getValueAt(row, 0);
            int age = Integer.parseInt(model.getValueAt(row, 1).toString());
            PreparedStatement stmt = con.prepareStatement("delete from users where age=? and name=?");
            stmt.setInt(1,age);
            stmt.setString(2, name);
            stmt.executeUpdate();
            listBtn.doClick();
        } catch (SQLException ex) {
            Logger.getLogger(ProjectJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_dleBtnActionPerformed

